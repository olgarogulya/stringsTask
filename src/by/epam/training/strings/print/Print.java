package by.epam.training.strings.print;

public class Print {

	public void print(String a) {
		System.out.println(a);
	}
	
	public void print(Integer a) {
		System.out.println(a);
	}
	
	public void print(Boolean a) {
		System.out.println(a);
	}
	
	public void print(StringBuilder a) {
		System.out.println(a);
	}
}
